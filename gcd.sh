#!/bin/bash

# 入力チェック
if [ $# -ne 2 ]; then
  echo "エラー: 引数は2つの自然数で指定してください。" >&2
  exit 1
fi

# 整数かつ自然数（0より大きい）かチェック
if ! [[ "$1" =~ ^[1-9][0-9]*$ ]] || ! [[ "$2" =~ ^[1-9][0-9]*$ ]]; then
  echo "エラー: 自然数（1以上の整数）を2つ指定してください。" >&2
  exit 1
fi

x=$1
y=$2

# 最大公約数計算　ユークリッドの互除法
while [ $y -ne 0 ]; do
  temp=$y
  y=$(( x % y ))
  x=$temp
done

echo "$x"
exit 0
